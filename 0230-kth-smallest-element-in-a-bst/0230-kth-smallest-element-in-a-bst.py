# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def kthSmallest(self, root: Optional[TreeNode], k: int) -> int:
        arr = []
        def preorder(root):
            if not root:
                return
            
            preorder(root.left)
            arr.append(root.val)
            if len(arr) == k:
                return
            preorder(root.right)

        preorder(root)

        return arr[k-1]
            


